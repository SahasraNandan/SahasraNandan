package com.core.java.Casting;

public class ExampleTypeCasting {
    public static void main(String arg[]) {

        int b=123;
        byte a=(byte)b;//casting means we need covert big values to small  values


        float f=5.6f;
        int num=(int)f;

        double d=7.6;
        int num2=(int)d;


        System.out.println("casting values+" + a);
        System.out.println("casting values+" + num);
        System.out.println("casting values+" + num2);

    }
}
